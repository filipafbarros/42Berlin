#include "ft_printf.h"

int	ft_print_nbr(int n)
{
	int	i;

	i = 1;
	if (n < 0)
	{
		ft_putchar_fd('-', 1);
		n *= -1;
		i++;
	}
	ft_putnbr_fd(n, 1);
	while (n > 9)
	{
		n /= 10;
		i++;
	}
	return (i);
}
