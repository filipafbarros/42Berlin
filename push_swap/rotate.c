#include "push_swap.h"

void	rotate(t_node **stack)
{
	t_node	*last_node;

	if (!(*stack) || !((*stack)->next))
		return ;
	last_node = get_last_node(*stack);
	last_node->next = *stack;
	*stack = (*stack)->next;
	(*stack)->prev = NULL;
	last_node->next->prev = last_node;
	last_node->next->next = NULL;
}

t_node	*get_last_node(t_node *stack)
{
	while (stack && ((stack)->next) != NULL)
		stack = stack->next;
	return (stack);
}

void	ra(t_node **a)
{
	rotate(a);
	ft_printf("ra\n");
}

void	rb(t_node **b)
{
	rotate(b);
	ft_printf("rb\n");
}

void	rr(t_node **a, t_node **b)
{
	rotate(a);
	rotate(b);
	ft_printf("rr\n");
}
